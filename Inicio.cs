        private void timer1_Tick(object sender, EventArgs e)
        {
            if (progressBar1.Value < 100)
            {
                progressBar1.Value++;
            }
            else
            {
                timer1.Enabled = false;
                Login chama = new Login();
                chama.Show();
                this.Visible = false;
            }
